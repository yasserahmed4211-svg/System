-- 3.3 Activity
CREATE TABLE MD_ACTIVITY (
    ID_PK                    NUMBER          NOT NULL,
    CODE                     VARCHAR2(50)    NOT NULL,
    NAME                     VARCHAR2(200)   NOT NULL,
    DESCRIPTION              VARCHAR2(500),
    DEFAULT_STOCK_UOM_ID_FK  NUMBER,
    CONVERSION_TYPE          VARCHAR2(20)    NOT NULL,
    REQUIRES_ACTUAL_WEIGHT   NUMBER(1)       NOT NULL,
    ALLOW_FRACTION           NUMBER(1)       NOT NULL,
    IS_ACTIVE                NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT               TIMESTAMP       NOT NULL,
    CREATED_BY               VARCHAR2(100)   NOT NULL,
    UPDATED_AT               TIMESTAMP,
    UPDATED_BY               VARCHAR2(100),
    CONSTRAINT MD_ACTIVITY_PK PRIMARY KEY (ID_PK),
    CONSTRAINT MD_ACTIVITY_UK UNIQUE (CODE),
    CONSTRAINT CK_MD_ACTIVITY_CONV CHECK (CONVERSION_TYPE IN ('FIXED', 'VARIABLE')),
    CONSTRAINT CK_MD_ACTIVITY_WEIGHT CHECK (REQUIRES_ACTUAL_WEIGHT IN (0, 1)),
    CONSTRAINT MD_ACTIVITY_WEIGHT_CHK CHECK ((CONVERSION_TYPE = 'VARIABLE' AND REQUIRES_ACTUAL_WEIGHT = 1) OR (CONVERSION_TYPE = 'FIXED')),
    CONSTRAINT CK_MD_ACTIVITY_FRAC CHECK (ALLOW_FRACTION IN (0, 1)),
    CONSTRAINT CK_MD_ACTIVITY_ACTIVE CHECK (IS_ACTIVE IN (0, 1)),
    CONSTRAINT MD_ACTIVITY_UOM_FK FOREIGN KEY (DEFAULT_STOCK_UOM_ID_FK) REFERENCES MD_LOOKUP_DETAIL (ID_PK)
);
CREATE INDEX IDX_MD_ACTIVITY_UOM_FK ON MD_ACTIVITY (DEFAULT_STOCK_UOM_ID_FK);
CREATE INDEX IDX_MD_ACTIVITY_ACTIVE ON MD_ACTIVITY (IS_ACTIVE);
CREATE INDEX IDX_MD_ACTIVITY_CODE   ON MD_ACTIVITY (CODE);

