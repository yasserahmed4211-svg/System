    NAME_EN           VARCHAR2(100)   NOT NULL,
    PARENT_ID         NUMBER(19),
    PERM_CODE         VARCHAR2(100),
    ROUTE_PATH        VARCHAR2(200),
    TENANT_ID         VARCHAR2(64)    NOT NULL,
