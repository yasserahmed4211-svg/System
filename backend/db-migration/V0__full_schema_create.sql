-- ============================================================
-- 2. SECURITY MODULE TABLES
-- ============================================================

-- 2.1 Users
CREATE TABLE USERS (
    ID                NUMBER GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    USERNAME          VARCHAR2(80)    NOT NULL,
    PASSWORD          VARCHAR2(200)   NOT NULL,
    ENABLED           NUMBER(1)       DEFAULT 1 NOT NULL,
    TENANT_ID         VARCHAR2(64)    NOT NULL,
    CREATED_AT        TIMESTAMP       NOT NULL,
    CREATED_BY        VARCHAR2(100)   NOT NULL,
    UPDATED_AT        TIMESTAMP,
    UPDATED_BY        VARCHAR2(100),
    CONSTRAINT USERS_PK PRIMARY KEY (ID),
    CONSTRAINT UK_USERS_TENANT_USERNAME UNIQUE (TENANT_ID, USERNAME),
    CONSTRAINT CK_USERS_ENABLED CHECK (ENABLED IN (0, 1))
);
CREATE INDEX IDX_USERS_TENANT   ON USERS (TENANT_ID);
CREATE INDEX IDX_USERS_ENABLED  ON USERS (ENABLED);
CREATE INDEX IDX_USERS_USERNAME ON USERS (USERNAME);

-- 2.2 Roles
CREATE TABLE ROLES (
    ID                NUMBER GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    NAME              VARCHAR2(60)    NOT NULL,
    IS_ACTIVE         NUMBER(1)       DEFAULT 1 NOT NULL,
    TENANT_ID         VARCHAR2(64)    NOT NULL,
    CREATED_AT        TIMESTAMP       NOT NULL,
    CREATED_BY        VARCHAR2(100)   NOT NULL,
    UPDATED_AT        TIMESTAMP,
    UPDATED_BY        VARCHAR2(100),
    CONSTRAINT ROLES_PK PRIMARY KEY (ID),
    CONSTRAINT UK_ROLES_TENANT_NAME UNIQUE (TENANT_ID, NAME),
    CONSTRAINT CK_ROLES_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);
CREATE INDEX IDX_ROLES_TENANT    ON ROLES (TENANT_ID);
CREATE INDEX IDX_ROLES_IS_ACTIVE ON ROLES (IS_ACTIVE);
CREATE INDEX IDX_ROLES_TENANT_ACTIVE ON ROLES (TENANT_ID, IS_ACTIVE);

-- 2.3 Pages (UI screens)
CREATE TABLE SEC_PAGES (
    ID_PK             NUMBER          NOT NULL,
    PAGE_CODE         VARCHAR2(50)    NOT NULL,
    NAME_AR           VARCHAR2(100)   NOT NULL,
    NAME_EN           VARCHAR2(100)   NOT NULL,
    ROUTE             VARCHAR2(200)   NOT NULL,
    ICON              VARCHAR2(50),
    MODULE            VARCHAR2(50),
    PARENT_ID_FK      NUMBER,
    DISPLAY_ORDER     NUMBER,
    IS_ACTIVE         NUMBER(1)       DEFAULT 1,
    DESCRIPTION       VARCHAR2(500),
    TENANT_ID         VARCHAR2(64)    NOT NULL,
    CREATED_AT        TIMESTAMP       NOT NULL,
    CREATED_BY        VARCHAR2(100)   NOT NULL,
    UPDATED_AT        TIMESTAMP,
    UPDATED_BY        VARCHAR2(100),
    CONSTRAINT SEC_PAGES_PK PRIMARY KEY (ID_PK),
    CONSTRAINT UK_PAGES_TENANT_CODE  UNIQUE (TENANT_ID, PAGE_CODE),
    CONSTRAINT UK_PAGES_TENANT_ROUTE UNIQUE (TENANT_ID, ROUTE),
    CONSTRAINT CK_PAGES_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);
CREATE INDEX IDX_PAGES_MODULE ON SEC_PAGES (MODULE);
CREATE INDEX IDX_PAGES_ACTIVE ON SEC_PAGES (IS_ACTIVE);
CREATE INDEX IDX_PAGES_PARENT ON SEC_PAGES (PARENT_ID_FK);
CREATE INDEX IDX_PAGES_TENANT ON SEC_PAGES (TENANT_ID);

-- 2.4 Permissions
CREATE TABLE PERMISSIONS (
    ID                NUMBER GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    NAME              VARCHAR2(150)   NOT NULL,
    PAGE_ID_FK        NUMBER,
    PERMISSION_TYPE   VARCHAR2(20),
    TENANT_ID         VARCHAR2(64)    NOT NULL,
    CREATED_AT        TIMESTAMP       NOT NULL,
    CREATED_BY        VARCHAR2(100)   NOT NULL,
    UPDATED_AT        TIMESTAMP,
    UPDATED_BY        VARCHAR2(100),
    CONSTRAINT PERMISSIONS_PK PRIMARY KEY (ID),
    CONSTRAINT UK_PERMS_TENANT_NAME UNIQUE (TENANT_ID, NAME),
    CONSTRAINT FK_PERMS_PAGE FOREIGN KEY (PAGE_ID_FK) REFERENCES SEC_PAGES (ID_PK)
);
CREATE INDEX IDX_PERMS_TENANT  ON PERMISSIONS (TENANT_ID);
CREATE INDEX IDX_PERMS_NAME    ON PERMISSIONS (NAME);
CREATE INDEX IDX_PERMS_PAGE_FK ON PERMISSIONS (PAGE_ID_FK);
CREATE INDEX IDX_PERMS_TYPE    ON PERMISSIONS (PERMISSION_TYPE);

-- 2.5 User ↔ Role join table
CREATE TABLE USER_ROLES (
    USER_ID           NUMBER NOT NULL,
    ROLE_ID           NUMBER NOT NULL,
    CONSTRAINT USER_ROLES_PK PRIMARY KEY (USER_ID, ROLE_ID),
    CONSTRAINT FK_UR_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ID)
);

-- 2.6 Role ↔ Permission join table
CREATE TABLE ROLE_PERMISSIONS (
    ROLE_ID           NUMBER NOT NULL,
    PERM_ID           NUMBER NOT NULL,
    CONSTRAINT ROLE_PERMISSIONS_PK PRIMARY KEY (ROLE_ID, PERM_ID),
    CONSTRAINT FK_RP_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ID),
    CONSTRAINT FK_RP_PERM FOREIGN KEY (PERM_ID) REFERENCES PERMISSIONS (ID)
);

-- 2.7 Refresh Tokens
CREATE TABLE REFRESH_TOKENS (
    ID                NUMBER GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    JTI               VARCHAR2(64)    NOT NULL,
    USER_ID           NUMBER          NOT NULL,
    TENANT_ID         VARCHAR2(64)    NOT NULL,
    CREATED_AT        TIMESTAMP       NOT NULL,
    EXPIRES_AT        TIMESTAMP       NOT NULL,
    REVOKED           NUMBER(1)       DEFAULT 0 NOT NULL,
    CONSTRAINT REFRESH_TOKENS_PK PRIMARY KEY (ID),
    CONSTRAINT UK_REFRESH_TOKENS_JTI UNIQUE (JTI),
    CONSTRAINT FK_RT_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT CK_RT_REVOKED CHECK (REVOKED IN (0, 1))
);

-- 2.8 Menu Items (sidebar navigation)
CREATE TABLE SEC_MENU_ITEM (
    ID                NUMBER GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    DESCRIPTION       VARCHAR2(500),
    DISPLAY_ORDER     NUMBER(10),
    ICON              VARCHAR2(50),
    IS_ACTIVE         NUMBER(1)       DEFAULT 1,
