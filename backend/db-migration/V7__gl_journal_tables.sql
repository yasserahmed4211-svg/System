-- ── Seed SEC_PAGES ──────────────────────────────────────────

-- GL Journal Page (child of GL parent)
MERGE INTO SEC_PAGES tgt
USING (SELECT 'default' AS TENANT_ID, 'GL_JOURNAL' AS PAGE_CODE FROM DUAL) src
  ON (tgt.TENANT_ID = src.TENANT_ID AND tgt.PAGE_CODE = src.PAGE_CODE)
WHEN NOT MATCHED THEN INSERT
  (ID_PK, TENANT_ID, PAGE_CODE, NAME_AR, NAME_EN, ROUTE, ICON, MODULE, PARENT_ID_FK, DISPLAY_ORDER, IS_ACTIVE, DESCRIPTION, CREATED_AT, CREATED_BY)
  VALUES (SEC_PAGES_SEQ.NEXTVAL, 'default', 'GL_JOURNAL', N'القيود اليومية', 'Journal Entries', '/finance/gl/journals', 'ti ti-notebook', 'FINANCE',
          (SELECT ID_PK FROM SEC_PAGES WHERE PAGE_CODE = 'GL' AND TENANT_ID = 'default'), 3, 1, 'GL Journal Entries management page', SYSTIMESTAMP, 'SYSTEM')
WHEN MATCHED THEN UPDATE SET
  tgt.NAME_AR = N'القيود اليومية', tgt.NAME_EN = 'Journal Entries',
  tgt.ROUTE = '/finance/gl/journals', tgt.ICON = 'ti ti-notebook', tgt.MODULE = 'FINANCE',
  tgt.PARENT_ID_FK = (SELECT ID_PK FROM SEC_PAGES WHERE PAGE_CODE = 'GL' AND TENANT_ID = 'default'),
  tgt.IS_ACTIVE = 1, tgt.UPDATED_AT = SYSTIMESTAMP, tgt.UPDATED_BY = 'SYSTEM';

