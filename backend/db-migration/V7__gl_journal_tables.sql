-- ── GL_JOURNAL_HDR ──────────────────────────────────────────
CREATE TABLE GL_JOURNAL_HDR (
    ID_PK                 NUMBER            NOT NULL,
    JOURNAL_NO            VARCHAR2(50)      NOT NULL,
    JOURNAL_DATE          DATE              NOT NULL,
    JOURNAL_TYPE_ID_FK    VARCHAR2(50)      NOT NULL,
    STATUS_ID_FK          VARCHAR2(50)      NOT NULL,
    DESCRIPTION           VARCHAR2(500),
    SOURCE_MODULE_ID_FK   VARCHAR2(50),
    SOURCE_DOC_TYPE_ID    VARCHAR2(50),
    SOURCE_DOC_ID_FK      NUMBER,
    SOURCE_POSTING_ID_FK  NUMBER,
    TOTAL_DEBIT           NUMBER(18,2)      DEFAULT 0 NOT NULL,
    TOTAL_CREDIT          NUMBER(18,2)      DEFAULT 0 NOT NULL,
    ACTIVE_FL             NUMBER(1)         DEFAULT 1 NOT NULL,
    CREATED_AT            TIMESTAMP         NOT NULL,
    CREATED_BY            VARCHAR2(100)     NOT NULL,
    UPDATED_AT            TIMESTAMP,
    UPDATED_BY            VARCHAR2(100),
    CONSTRAINT GL_JOURNAL_HDR_PK PRIMARY KEY (ID_PK),
    CONSTRAINT UK_GL_JOURNAL_NO UNIQUE (JOURNAL_NO),
    CONSTRAINT CK_GL_JOURNAL_ACTIVE CHECK (ACTIVE_FL IN (0, 1))
);

CREATE INDEX IDX_GL_JOURNAL_HDR_DATE       ON GL_JOURNAL_HDR (JOURNAL_DATE);
CREATE INDEX IDX_GL_JOURNAL_HDR_TYPE_FK    ON GL_JOURNAL_HDR (JOURNAL_TYPE_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_HDR_STATUS_FK  ON GL_JOURNAL_HDR (STATUS_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_HDR_SRC_MOD    ON GL_JOURNAL_HDR (SOURCE_MODULE_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_HDR_ACTIVE     ON GL_JOURNAL_HDR (ACTIVE_FL);
CREATE INDEX IDX_GL_JOURNAL_HDR_POST_FK    ON GL_JOURNAL_HDR (SOURCE_POSTING_ID_FK);

-- ── GL_JOURNAL_LINE ─────────────────────────────────────────
CREATE TABLE GL_JOURNAL_LINE (
    ID_PK                 NUMBER            NOT NULL,
    JOURNAL_ID_FK         NUMBER            NOT NULL,
    LINE_NO               NUMBER(5)         NOT NULL,
    ACCOUNT_ID_FK         NUMBER            NOT NULL,
    DEBIT_AMOUNT          NUMBER(18,2),
    CREDIT_AMOUNT         NUMBER(18,2),
    CUSTOMER_ID_FK        NUMBER,
    SUPPLIER_ID_FK        NUMBER,
    COST_CENTER_ID_FK     NUMBER,
    DESCRIPTION           VARCHAR2(500),
    CREATED_AT            TIMESTAMP         NOT NULL,
    CREATED_BY            VARCHAR2(100)     NOT NULL,
    UPDATED_AT            TIMESTAMP,
    UPDATED_BY            VARCHAR2(100),
    CONSTRAINT GL_JOURNAL_LINE_PK PRIMARY KEY (ID_PK),
    CONSTRAINT GL_JOURNAL_LINE_HDR_FK FOREIGN KEY (JOURNAL_ID_FK)
        REFERENCES GL_JOURNAL_HDR (ID_PK)
);

CREATE INDEX IDX_GL_JOURNAL_LINE_HDR_FK    ON GL_JOURNAL_LINE (JOURNAL_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_LINE_ACCT_FK   ON GL_JOURNAL_LINE (ACCOUNT_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_LINE_CUST_FK   ON GL_JOURNAL_LINE (CUSTOMER_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_LINE_SUPP_FK   ON GL_JOURNAL_LINE (SUPPLIER_ID_FK);
CREATE INDEX IDX_GL_JOURNAL_LINE_CC_FK     ON GL_JOURNAL_LINE (COST_CENTER_ID_FK);

